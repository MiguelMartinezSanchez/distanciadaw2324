/**
 * Suma dos números.
 *
 * @param int $a El primer número.
 * @param int $b El segundo número.
 * @return int La suma de los dos números.
 */
function sumar($a, $b) {
    return $a + $b;
}

/**
 * Multiplica dos números.
 *
 * @param int $a El primer número.
 * @param int $b El segundo número.
 * @return int El producto de los dos números.
 */
function multiplicar($a, $b) {
    return $a * $b;
}
